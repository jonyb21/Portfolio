import assert from "node:assert/strict";
import fs from "node:fs";

const index = fs.readFileSync("public/index.html", "utf8");
const work = fs.readFileSync("public/work.html", "utf8");
const product = fs.readFileSync("public/product.html", "utf8");
const about = fs.readFileSync("public/about.html", "utf8");
const contact = fs.readFileSync("public/contact.html", "utf8");
const css = fs.readFileSync("public/styles.css", "utf8");
const admin = fs.readFileSync("public/admin.html", "utf8");
const adminJs = fs.readFileSync("public/admin.js", "utf8");
const readme = fs.readFileSync("README.md", "utf8");
const site = JSON.parse(fs.readFileSync("data/site.json", "utf8"));
new Function(adminJs);

assert(!/[—–]/.test(index + work + product + about + contact + css + admin + JSON.stringify(site)), "No em dash or en dash in shipped UI");
assert(!index.includes('id="work"'), "Home page is landing hero only");
assert(work.includes('body data-page="work"'), "Work tab is a separate page");
assert(about.includes('body data-page="about"'), "About tab is a separate page");
assert(contact.includes('body data-page="contact"'), "Contact tab is a separate page");
assert(index.includes("min-h") === false, "Viewport height is owned by CSS");
assert(css.includes("min-height: 100dvh"), "Uses stable dynamic viewport height");
assert(css.includes("--accent: #c6d875"), "Olive accent matches the reference");
assert(css.includes("grid-template-columns: repeat(3"), "Selected work uses a three-card desktop grid");
assert(site.projects.length === 4, "Work page includes the hero chair plus three selected work cards");
assert(site.nav.every(item => item.href.startsWith("/")), "Top nav uses page URLs, not anchors");
assert(site.hero.image === "/assets/furniture/hero-lounge-chair.png", "Hero uses generated furniture image");
assert(site.projects.some(project => project.image === site.hero.image), "Hero chair image is also listed as a work project");
assert(index.includes(site.hero.body), "Home fallback copy matches site data");
assert(work.includes(`<span data-field="workCta">${site.workCta}</span>`), "Work fallback CTA matches site data");
assert(contact.includes(`mailto:${site.contact.email}`), "Contact fallback email matches site data");
assert(fs.readFileSync("server.js", "utf8").includes("valid email address"), "Server rejects invalid contact emails");
assert(fs.readFileSync("server.js", "utf8").includes("function present(value)"), "Server rejects whitespace-only content fields");
assert(!/[→←]|-&gt;/.test(index + work + product + about + contact + fs.readFileSync("public/app.js", "utf8")), "Arrows use HTML entities for stable rendering");
assert(site.projects.every(project => project.image.startsWith("/assets/furniture/")), "Project images use generated furniture assets");
assert(site.projects.every(project => project.href === `/work/${project.slug}`), "Each project links to its own page");
assert(fs.readFileSync("server.js", "utf8").includes("Project slugs must be unique"), "Server rejects duplicate project slugs");
assert(fs.readFileSync("server.js", "utf8").includes("at least three detail notes"), "Server keeps product pages detailed enough");
assert(fs.readFileSync("server.js", "utf8").includes("Image path does not exist"), "Server rejects broken local image paths");
assert(fs.readFileSync("server.js", "utf8").includes("Nav links") && fs.readFileSync("server.js", "utf8").includes("separate page paths"), "Server keeps top nav on separate pages");
assert(fs.readFileSync("server.js", "utf8").includes("points to a missing page"), "Server rejects dead internal page links");
assert(fs.readFileSync("server.js", "utf8").includes("is required"), "Server gives useful errors for blank page links");
assert(adminJs.includes("href: `/work/${slug}`"), "Admin keeps project links tied to slugs");
assert(!adminJs.includes('data-project="${index}" data-key="href"'), "Project URLs are generated, not manually edited");
assert(adminJs.includes("(await response.json()).error"), "Admin shows server validation errors");
assert(site.hero.detailImage === "/assets/furniture/hero-lounge-chair-detail.png", "Hero has generated detail image");
assert(site.projects.every(project => project.detailImage?.startsWith("/assets/furniture/")), "Projects use generated detail crop assets");
assert(site.projects.every(project => project.summary && project.notes?.length >= 3), "Projects have finished portfolio copy");
assert(site.about.experience.length >= 3, "About page has relevant experience entries");
assert(site.about.experienceTitle === "Relevant Experience", "About page frames work history as relevant experience");
assert(site.about.experience.some(item => /Prototyping/.test(item.role)), "About page includes hands-on prototyping experience");
assert(fs.readFileSync("server.js", "utf8").includes("role, company, period, and description"), "Server keeps about experience rows complete");
assert(about.includes('id="experience-list"'), "About page renders experience list");
assert(!work.includes('id="project-details"'), "Work page lists projects without inline detail sections");
assert(product.includes('id="product-page"'), "Project template renders project detail pages");
assert(!admin.includes("Site JSON"), "Admin UI does not expose raw JSON editing");
assert(admin.includes('data-tab="home"'), "Admin UI has interactive section tabs");
assert(admin.includes('id="projects-editor"'), "Admin UI has project form editing");
assert(fs.readFileSync("public/app.js", "utf8").includes('page === "product" ? "work" : page'), "Product pages keep Work nav active");
assert(adminJs.includes("function nextProjectSlug()"), "New admin projects get duplicate-safe default slugs");
assert(adminJs.includes("const slug = nextProjectSlug()"), "New admin projects use the duplicate-safe slug helper");
assert(adminJs.includes("href: `/work/${slug}`"), "New admin projects default to product pages");
assert(adminJs.includes("Add the key material or construction detail."), "New admin projects start with useful detail notes");
assert(readme.includes("Each project needs materials and at least three notes"), "README documents project detail requirements");
assert(readme.includes("Role | Company | Period | Description"), "README documents experience row format");
